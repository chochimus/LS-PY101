# Before running: one is two, two is three, three is one, this time, by accessing
# the index we are mutating the values of the object passed so they change the original 
# object.

def mess_with_vars(one, two, three):
    one[0] = "two"
    two[0] = "three"
    three[0] = "one"

one = ["one"]
two = ["two"]
three = ["three"]

